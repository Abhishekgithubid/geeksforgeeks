class Solution {
    ArrayList<Long> nthRowOfPascalTriangle(int n) {
        // code here
        ArrayList<Long> list = new ArrayList<>();
        long ans = 1;
        long mod = 1000000007;
        list.add(ans);
        
        for(int col = 1; col < n; col++)
        {
            ans = ans * (n - col);
            ans = ans / col;
            list.add(ans % mod);
        }
        return list;
    }
    
}